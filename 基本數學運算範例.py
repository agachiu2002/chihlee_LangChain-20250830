# Python 基本數學運算範例 - 使用繁體中文變數名稱

# 定義變數
a = 7
b = 3

print("=== 基本數學運算範例 ===")
print(f"變數 a = {a}")
print(f"變數 b = {b}")
print()

# 加法運算
加法結果 = a + b
print(f"加法運算: {a} + {b} = {加法結果}")

# 減法運算
減法結果 = a - b
print(f"減法運算: {a} - {b} = {減法結果}")

# 乘法運算
乘法結果 = a * b
print(f"乘法運算: {a} × {b} = {乘法結果}")

# 除法運算
除法結果 = a / b
print(f"除法運算: {a} ÷ {b} = {除法結果}")

# 整除運算
整除結果 = a // b
print(f"整除運算: {a} ÷ {b} = {整除結果} (取整數部分)")

# 取餘數運算
餘數結果 = a % b
print(f"取餘數運算: {a} % {b} = {餘數結果}")

# 次方運算
次方結果 = a ** b
print(f"次方運算: {a} 的 {b} 次方 = {次方結果}")

print("\n=== 更多範例 ===")

# 使用更多繁體中文變數名稱
第一個數字 = 10
第二個數字 = 4
第三個數字 = 2

# 複合運算
總和 = 第一個數字 + 第二個數字 + 第三個數字
print(f"三個數字相加: {第一個數字} + {第二個數字} + {第三個數字} = {總和}")

# 平均值計算
平均值 = 總和 / 3
print(f"平均值: {總和} ÷ 3 = {平均值:.2f}")

# 面積計算 (長方形)
長度 = 8
寬度 = 5
面積 = 長度 * 寬度
print(f"長方形面積: 長度 {長度} × 寬度 {寬度} = {面積}")

# 圓周率計算
半徑 = 6
圓周率 = 3.14159
圓面積 = 圓周率 * 半徑 ** 2
print(f"圓面積: π × {半徑}² = {圓面積:.2f}")

# 溫度轉換 (攝氏轉華氏)
攝氏溫度 = 25
華氏溫度 = 攝氏溫度 * 9 / 5 + 32
print(f"溫度轉換: {攝氏溫度}°C = {華氏溫度}°F")

# 購物計算
商品單價 = 150
購買數量 = 3
折扣率 = 0.9  # 9折
小計 = 商品單價 * 購買數量
折扣後金額 = 小計 * 折扣率
print(f"購物計算:")
print(f"  商品單價: {商品單價} 元")
print(f"  購買數量: {購買數量} 個")
print(f"  小計: {小計} 元")
print(f"  折扣後: {折扣後金額} 元")

print("\n=== 運算優先順序範例 ===")
# 括號優先
括號運算 = (a + b) * 2
print(f"括號運算: ({a} + {b}) × 2 = {括號運算}")

# 次方優先
次方優先 = a + b ** 2
print(f"次方優先: {a} + {b}² = {次方優先}")

# 乘除優先於加減
乘除優先 = a + b * 2
print(f"乘除優先: {a} + {b} × 2 = {乘除優先}")

print("\n=== 實用計算範例 ===")

# 時間計算
總秒數 = 3661
小時 = 總秒數 // 3600
分鐘 = (總秒數 % 3600) // 60
秒數 = 總秒數 % 60
print(f"時間轉換: {總秒數} 秒 = {小時} 小時 {分鐘} 分鐘 {秒數} 秒")

# 距離計算
速度 = 60  # 公里/小時
時間 = 2.5  # 小時
距離 = 速度 * 時間
print(f"距離計算: 速度 {速度} km/h × 時間 {時間} 小時 = {距離} 公里")

# 百分比計算
原價 = 1000
漲幅 = 0.15  # 15%
新價 = 原價 * (1 + 漲幅)
漲價金額 = 原價 * 漲幅
print(f"漲價計算: 原價 {原價} 元，漲幅 {漲幅*100}%，新價 {新價} 元，漲價 {漲價金額} 元")
